    History
  </div>

  <p>In 1957 the University of Groningen sends a request to the Minister of Education to institute doctoral exams for applied physics. In 1958 the request is granted. Professors for applied physics and chemistry are found easily, but for applied physics the search is harder.
  In November 1961 Jan Francken receives a letter from the nomination committee in which he is asked to become a professor. In that time he is working at Philips on the development of color TV. He accepts the request and on July 3, 1962 he is appointed professor. His main task is to set up the study applied physics.</p>
  <p>In the beginning the study is accommodated at the Westersingel, but due to the rapid growth of the number of students, expansion is needed and eventually they end up at the Antillenstraat. In 1968 they move again, this time to the current location.</p>
  <p>In an attempt to bind the engineer’s title to the association the name is changed to “Vereniging van Toekomstige Ingenieurs” (Association of Future Engineers). This goal is accomplished in 1971 and the association is renamed to “Vereniging voor Kandidaten Technische Natuurkunde” (Association for Candidates Applied Physics). Between 1982 and 1984 the study is reformed and the students do not have to take candidate exams anymore, so the current name of the association loses its meaning.</p>

  <div class="well" style="text-align: center">
    <h2>Establishment of T.F.V. ‘Professor Francken’</h2>
    <h5>October 12, 1984</h5>
  </div>

  <p>On August 1, 1984 professor Francken retires. After his valedictory speech on September 18, he is asked if he would like to bind his name to the association. On October 12, 1984 the association T.F.V. ‘Professor Francken’ is founded and in 1985 professor Francken is appointed honorary member of the association. The current logo (the FCC-structure with the logo of the university) is introduced in 1988.</p>

  <p>In honor of the lustrum in 1989, the first Francken symposium is organized. Since then it is an annual event. In the same year the first foreign trip takes place and also becomes annual. In 1990 the board choses young grain jenever from Hooghoudt as their standard business gift. In 1992 the association receives their first member’s room.</p>

  <p>The board of ’93-’94 writes a letter to the Minister of Education with the request to add a fifth year to the study applied physics and the request is granted. There is decided that the association needs an association magazine. Consequently the first edition of the Francken Vrij appears in 1996.</p>

  <div class="well" style="text-align: center">
    <h2>Publication of the first Francken Vrij</h2>
    <h5>1996</h5>
  </div>

  <p>After the annual Francken Symposium in 1998 professor De Hosson is appointed honorary member of the association thanks to his commitment to and support of the association. He receives the corresponding tie.</p>

  <p>The first lustrum foreign trip outside Europe is organized in 2000 and has South Africa as destination. Since then it is tradition to organize an foreign trip outside of Europe in every lustrum year.</p>

  <p>In 2002 the current member’s room is taken into use. Two years later, in 2004 the first engineer’s symposium is organized in cooperation with the associations TBV Lugus and GTD Bernoulli.</p>

  <p>The flexible bachelor’s course starts in 2006 and thanks to that the percentage of women increases significantly. In this year the association also starts offering practicing sessions for exams.</p>


  <div class="well" style="text-align: center">
    <h2>Jan Francken dies at age 87</h2>
    <h5>January 15, 2007</h5>
  </div>

  <p>After a period of two years at cafe Koster, cafe Karakter is appointed regular pub in 2010. In the same year the consultancy event Expedition Strategy is organized for the first time in cooperation with the associations VESTING, Risk and TBV Lugus. The university launches the binding recommendation, which means that students have to collect 40 study points in their first year in order for them to continue their study.</p>

  <p>In 2012 the first Francken almanac appears. On May 15, 2013 the almanac receives the price UK Almanac Award for best almanac. The second edition of the Francken almanac appears in October 2013.</p>

